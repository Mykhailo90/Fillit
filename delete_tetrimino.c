char	**delete_tetrimoino(char **map, char ch)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (map[i])
	{
		n = 0;
		while (map[i][n] != '\0')
		{
			if (map[i][n] == ch)
			{
				map[i][n] = '.';
			}
			n++;
		}
		i++;
	}
	return (map);
}
