#include "funclib.h"

void		*make_point(void *s, int c, size_t n)
{
	size_t	i;
	char	*s1;

	s1 = s;
	i = 0;
	while (i < n)
	{
		s1[i] = c;
		i++;
	}
	return (s);
}
